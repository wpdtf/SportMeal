CREATE PROCEDURE [dbo].[ПроцедураАвторизации]
    @Логин NVARCHAR(50),
    @ХешПароля NVARCHAR(100)
AS
BEGIN
    -- Проверяем клиента
    IF EXISTS (
        SELECT 1 
        FROM Клиенты к
        JOIN Пользователи п ON к.ИдПользователя = п.ИдПользователя
        WHERE п.Логин = @Логин AND п.ХешПароля = @ХешПароля
    )
    BEGIN
        SELECT 'Client' AS UserType, к.ИдКлиента AS Id
        FROM Клиенты к
        JOIN Пользователи п ON к.ИдПользователя = п.ИдПользователя
        WHERE п.Логин = @Логин AND п.ХешПароля = @ХешПароля
    END
    -- Проверяем сотрудника
    ELSE IF EXISTS (
        SELECT 1 
        FROM Сотрудники с
        JOIN Пользователи п ON с.ИдПользователя = п.ИдПользователя
        WHERE п.Логин = @Логин AND п.ХешПароля = @ХешПароля
    )
    BEGIN
        SELECT 'Employee' AS UserType, с.ИдСотрудника AS Id
        FROM Сотрудники с
        JOIN Пользователи п ON с.ИдПользователя = п.ИдПользователя
        WHERE п.Логин = @Логин AND п.ХешПароля = @ХешПароля
    END
    ELSE
    BEGIN
        SELECT NULL AS UserType, NULL AS Id
    END
END
GO
