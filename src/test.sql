-- Обновление процедуры регистрации клиента
CREATE PROCEDURE [dbo].[ПроцедураРегистрацииКлиента]
    @Логин NVARCHAR(50),
    @ХешПароля NVARCHAR(255),
    @Имя NVARCHAR(50),
    @Фамилия NVARCHAR(50),
    @Телефон NVARCHAR(20),
    @Почта NVARCHAR(100)
AS
BEGIN
    BEGIN TRANSACTION;
    BEGIN TRY
        -- Проверка существования логина
        IF EXISTS (SELECT 1 FROM Пользователи WHERE Логин = @Логин)
        BEGIN
            THROW 50001, 'Пользователь с таким логином уже существует', 1;
        END

        -- Создание записи пользователя
        INSERT INTO Пользователи (Логин, ХешПароля, ТипПользователя, Активен)
        VALUES (@Логин, @ХешПароля, 'Клиент', 1);

        DECLARE @ИдПользователя INT = SCOPE_IDENTITY();

        -- Создание записи клиента
        INSERT INTO Клиенты (ИдПользователя, Имя, Фамилия, Телефон, Почта, ДатаРегистрации)
        VALUES (@ИдПользователя, @Имя, @Фамилия, @Телефон, @Почта, GETDATE());

        DECLARE @ИдКлиента INT = SCOPE_IDENTITY();

        -- Возвращаем созданного клиента
        SELECT 
            к.ИдКлиента AS Id,
			к.ИдПользователя AS UserId,
            к.Имя AS FirstName,
            к.Фамилия AS LastName,
            к.Телефон AS Phone,
            к.Почта AS Email,
            к.ДатаРегистрации AS RegistrationDate,
            п.Логин AS Login,
            п.ХешПароля AS PasswordHash
        FROM Клиенты к
        INNER JOIN Пользователи п ON к.ИдПользователя = п.ИдПользователя
        WHERE к.ИдКлиента = @ИдКлиента;

        COMMIT;
    END TRY
    BEGIN CATCH
        ROLLBACK;
        THROW;
    END CATCH
END;
GO
